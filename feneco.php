                <p>O feneco, também conhecido como raposa-do-deserto, é uma pequena raposa crepuscular encontrada no
                    deserto do Saara e na Península do Sinai. É o menor de todos os canídeos e possui inúmeras
                    adaptações ao clima desértico, como seu corpo pequeno, patas peludas e orelhas excepcionalmente
                    grandes, que ajudam a regular o calor. Sua pelagem e funções renais são adaptadas às altas
                    temperaturas e à escassez de água, e possui audição sensível para detectar presas se movendo no
                    subsolo. É um animal onívoro que se alimenta de invertebrados, pequenos vertebrados, frutas e
                    tubérculos.
                </p>
                <p>Fenecos possuem orelhas excepcionalmente grandes que representam 20% da sua superfície corporal. Tem
                    um comprimento médio de cabeça-corpo de 24 a 41 cm e uma cauda que varia de 18 a 31 cm. O peso varia
                    de 0,68 a 1,6 kg, sendo as fêmeas ligeiramente menores que os machos. Sua pelagem é amarelada na
                    parte dorsal e branca nas partes inferiores e nos membros. As orelhas têm listras avermelhadas no
                    dorso e as bordas e o interior são brancos. Possui olhos grandes e escuros, com uma linha escura que
                    vai do canto interno até o focinho, que é preto.
                </p>
                <p>O feneco é um animal bem adaptado ao seu ambiente desértico. Sua pelagem densa ajuda a mantê-lo
                    aquecido à noite, enquanto suas orelhas grandes e pés peludos ajudam a dissipar o calor durante o
                    dia. Além disso, seus rins são capazes de filtrar urina altamente concentrada, permitindo que o
                    animal conserve água em um ambiente onde a água é escassa.
                </p>
