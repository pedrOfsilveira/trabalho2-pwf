                <p>Além das regiões desérticas, os fenecos também podem ser encontrados em áreas semiáridas e rochosas,
                    bem como em planícies de savana. São animais solitários e noturnos, evitando as horas mais quentes
                    do dia. Durante o dia, eles geralmente descansam em tocas ou em buracos no chão. Eles são animais
                    muito adaptados às condições extremas do deserto, sendo capazes de sobreviver por longos períodos
                    sem água, obtendo a maior parte de sua hidratação a partir de alimentos. Sua dieta é variada e
                    inclui principalmente pequenos animais, como roedores, aves, insetos e lagartos, além de frutas e
                    sementes.
                </p>
                <p>Um feneco cava sua toca na areia, seja em áreas abertas ou em locais protegidos por plantas com dunas
                    de areia estáveis. Em solos compactados, as tocas têm até 120 m2 de largura, com até 15 entradas
                    diferentes. Os fenecos são considerados moderadamente sociais, mas esta evidência é baseada
                    principalmente em animais em cativeiro. Essas raposas vivem em pequenas comunidades, cada uma
                    habitada por talvez dez indivíduos.
                </p>
