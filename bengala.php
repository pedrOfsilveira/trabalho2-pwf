                <p>A unidade social fundamental das raposas-de-bengala é o casal reprodutor, que pode permanecer junto
                    por vários anos. Às vezes, agregações maiores podem ocorrer quando os filhotes crescem e permanecem
                    por mais tempo na comunidade de origem. Essas raposas geralmente não têm medo de humanos, podem ser
                    facilmente domesticadas e podem ser encontradas próximas a áreas habitadas por pessoas.
