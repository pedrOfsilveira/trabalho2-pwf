                <p>A raposa-de-bengala (Vulpes bengalensis) é uma espécie de raposa endêmica ao subcontinente indiano,
                    do sopé do Himalaia e Terai do Nepal ao sul da Índia, e do sul e leste do Paquistão ao leste da
                    Índia e sudeste de Bangladesh.
                </p>
                <p>A raposa-de-bengala é uma raposa pequena com pelagem que varia de amarelo claro a cinza prateado. Ela
                    tem um focinho alongado, orelhas longas e pontudas, e uma cauda espessa com ponta preta,
                    representando cerca de 50-60% do comprimento total. Suas pernas são acastanhadas ou ruivas, e a
                    parte inferior é mais clara. As orelhas possuem dorso marrom escuro com margem preta e interior
                    branco. A variação na cor da pelagem é ampla, mas geralmente varia de cinza a marrom claro. Ela tem
                    em média 46 cm de comprimento da cabeça ao corpo, com uma cauda longa de 25 cm, e seu peso médio é
                    de 2,3-4,1 kg.
                </p>
                <p>A raposa-de-bengala é encontrada principalmente no subcontinente indiano, abrangendo uma área que vai
                    do sopé do Himalaia e Terai nepalense até o sul da Península Indiana, leste do Paquistão, leste da
                    Índia e sudeste de Bangladesh. Ela pode ser encontrada até altitudes de 1.500 metros no Nepal e
                    nordeste da Índia. No entanto, não há registros de sua presença no Afeganistão, Irã ou nas Gates
                    Ocidentais da Índia.
                </p>
