                <p> As raposas-anãs são principalmente noturnas, mas também podem ser ativas ao amanhecer e ao
                    entardecer. Elas preferem viver em pares ou pequenos grupos de parentes, não sendo territorialistas.
                    Utilizam tocas durante o ano para descansar, evitar predadores, proteger-se do calor extremo e criar
                    seus filhotes. Podem cavar suas próprias tocas ou modificar as tocas de outros animais. Em média,
                    uma raposa-anã pode usar até 11 tocas por ano. Elas têm a habilidade de escapar e mudar de direção
                    rapidamente, o que pode criar a ilusão de velocidade, mas se cansam facilmente e ficam exaustas. Em
                    uma medição precisa, a velocidade de uma raposa-anã foi registrada em cerca de 40 km/h na frente de
                    um carro.
                </p>
