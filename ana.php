                <p>A raposa-anã (Vulpes macrotis) é uma espécie de raposa que habita as regiões áridas e semi-áridas da
                    América do Norte, principalmente no sudoeste dos Estados Unidos e no norte e centro do México. É a
                    menor das três espécies de Vulpes existentes na América do Norte.
                </p>
                <p>A raposa-do-deserto é uma espécie com orelhas grandes, medindo entre 71 a 95 mm, que desempenham um
                    papel importante na dissipação do calor e proporcionam uma audição excepcional, semelhante à do
                    feneco. Essa espécie apresenta pouco dimorfismo sexual, sendo que o macho geralmente é ligeiramente
                    maior. O peso médio varia de 1,6 a 2,7 kg. O comprimento do corpo varia de 455 a 535 mm, e a cauda é
                    longa, representando cerca de 40% do comprimento total, adicionando mais 260-323 mm.
                </p>
                <p>A cor e a textura da pelagem podem variar geograficamente. Em geral, a pelagem dorsal é cinza ou
                    cinza-amarelada. As solas das patas são protegidas por tufos de pelos rígidos, que auxiliam no
                    deslocamento em superfícies arenosas e oferecem proteção contra temperaturas extremas. O focinho e
                    as vibrissas (pelos táteis) são geralmente pretos ou marrons. A cauda é espessa e cinza, com a ponta
                    preta, e a glândula caudal apresenta uma mancha preta pronunciada. Diferentemente da
                    raposa-cinzenta, a raposa-do-deserto não possui uma faixa ao longo da cauda. A cor da pelagem pode
                    variar do amarelo ao cinza, sendo que o dorso geralmente é mais escuro do que o restante da pelagem.
                </p>
