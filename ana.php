                <p>
                    A barriga e as orelhas internas são geralmente mais claras. A raposa-do-deserto apresenta manchas
                    escuras distintas ao redor do nariz. As orelhas são castanho-amareladas ou cinzentas, com base
                    amarelo-claro ou laranja. Os ombros, as partes inferiores, os flancos e uma faixa de aproximadamente
                    25 mm de largura no peito variam em cor de amarelo claro a laranja.
                </p>
                <p>A área mais ao norte é o interior árido do Oregon, e seu limite oriental é o sudoeste do Colorado.
                    Ela pode ser encontrada ao sul através de Nevada, Utah, sudeste da Califórnia, Arizona, Novo México
                    e oeste do Texas. As raposas-anãs habitam regiões áridas e semi-áridas, como desertos, chaparral e
                    pastagens, preferindo áreas com cobertura do solo esparsa. A vegetação varia regionalmente, com
                    exemplos como artemísia e arbustos Atriplex polycarpa. Elas constroem tocas em solos de textura
                    solta e podem ser encontradas em áreas agrícolas, especialmente em pomares, e até mesmo em áreas
                    urbanas, embora em menor quantidade.
                </p>
