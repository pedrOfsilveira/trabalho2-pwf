        <p>O gênero Vulpes é composto por mamíferos onívoros da família Canidae, conhecidos como "raposas verdadeiras".
          Esses animais são menores que os membros do gênero Canis e têm crânios mais achatados. As raposas verdadeiras
          são identificadas por suas marcas pretas triangulares entre os olhos e o nariz, bem como pelas pontas de suas
          caudas que geralmente têm uma cor diferente da pelagem. A expectativa de vida típica para este grupo varia de
          dois a quatro anos, mas pode ser de até uma década. Existem cerca de 12 espécies diferentes de raposas, cada
          uma com características físicas distintas e habitats específicos.
        </p>
        <p>Além da raposa-vermelha (Vulpes vulpes), que é a mais conhecida,, outras espécies de raposas incluem a
          raposa-do-ártico (Vulpes lagopus), a raposa-do-deserto (Vulpes zerda), a raposa-anã (Vulpes
          macrotis), a raposa-das-estepes (Vulpes corsac), a raposa-veloz (Vulpes velox), entre outras.
        </p>
        <p>As raposas são geralmente animais solitários, exceto durante o período de acasalamento ou quando a fêmea está
          criando seus filhotes. Elas são animais territoriais, demarcando seu espaço com urina e fezes e lutando contra
          outras raposas que invadem seu território. São animais inteligentes e ágeis, bem adaptados para viver em
          ambientes variados, desde desertos
          e tundras até florestas e áreas urbanas. Algumas espécies de raposas, como a raposa-vermelha, se adaptaram bem
          às mudanças feitas pelos seres humanos em seu habitat natural, e podem ser encontradas em áreas rurais e
          urbanas em todo o mundo.
        </p>
